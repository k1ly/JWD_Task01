public class NullSimplePointException extends Exception {

    public NullSimplePointException() {
        super();
    }

    public NullSimplePointException(String message) {
        super(message);
    }

    public NullSimplePointException(String message, Throwable cause) {
        super(message, cause);
    }

    public NullSimplePointException(Throwable cause) {
        super(cause);
    }
}
